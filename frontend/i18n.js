  let locale = localStorage.getItem("vt_locale") || "zh";

  function t(key, params) {
    const s = (STRINGS[locale] && STRINGS[locale][key]) || (STRINGS.zh[key]) || key;
    if (!params) return s;
    return s.replace(/\{(\w+)\}/g, (_, k) => (params[k] != null ? params[k] : `{${k}}`));
  }

  function setLocale(l) {
    locale = STRINGS[l] ? l : "zh";
    localStorage.setItem("vt_locale", locale);
    document.documentElement.lang = locale;
  }

  function getLocale() { return locale; }

  function applyPageI18n() {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      el.placeholder = t(el.getAttribute("data-i18n-placeholder"));
    });
    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      el.title = t(el.getAttribute("data-i18n-title"));
    });
  }

  window.I18n = { t, setLocale, getLocale, applyPageI18n };
